        <h4 class="fw-semibold mb-3 text-center"  style="color:#08317a;">Placement Policy</h4>

        <p>
          Final Placements at IMT Hyderabad are conducted for second-year students starting in the month of October. At IMTH, a <strong>“one student, one offer”</strong> policy is followed. A student, after being placed, is signed out of the placement process.
        </p>
        <p>
          A student who receives a <strong>Pre-Placement Offer (PPO)</strong> during internship or before the placement process, and the offer salary is <strong>greater than the institute’s last year’s average CTC</strong>, is <strong>bound to accept</strong> the offer and will be considered out of the placement process.
        </p>
        <p>
          However, if the PPO offered is <strong>less than the institute’s last year’s average CTC</strong>, the student has the right to either <strong>accept or decline</strong> the offer.
        </p>

