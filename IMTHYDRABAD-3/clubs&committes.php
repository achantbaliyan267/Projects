                    { name: "Student 5", batch: "2024-26", img: "https://imt.arawalihome.in/media/img/b.webp" },
                    { name: "Student 6", batch: "2024-26", img: "https://imt.arawalihome.in/media/img/b.webp" },
                    { name: "Student 7", batch: "2024-26", img: "https://imt.arawalihome.in/media/img/b.webp" }
                ]
                     }
        };

        // =========================================
        // LOGIC: STUDENT SUB-SLIDER (Inside Committee Detail)
        // =========================================
        let studentIdx = 0;
        const studentTrack = document.getElementById('studentTrack');
        const sPrev = document.getElementById('studentPrev');
        const sNext = document.getElementById('studentNext');

        function updateStudentSlider() {
            if (!studentTrack || !studentTrack.children.length) return;
            // Calculate visible cards based on screen width
            const visible = window.innerWidth >= 992 ? 3 : 1;
            const maxIdx = Math.max(0, studentTrack.children.length - visible);
            // Ensure index stays in bounds
            studentIdx = Math.min(Math.max(0, studentIdx), maxIdx);

            const cardW = studentTrack.children[0].getBoundingClientRect().width;
            const gap = 20; // matches CSS gap
            studentTrack.style.transform = `translateX(-${studentIdx * (cardW + gap)}px)`;

            if (sPrev) sPrev.disabled = (studentIdx === 0);
            if (sNext) sNext.disabled = (studentIdx >= maxIdx);
        }

        // Attach listeners ONLY ONCE
        if (sPrev && sNext) {
            sPrev.addEventListener('click', () => { if (studentIdx > 0) { studentIdx--; updateStudentSlider(); }});
            sNext.addEventListener('click', () => { 
                 const visible = window.innerWidth >= 992 ? 3 : 1;
                 const maxIdx = Math.max(0, studentTrack.children.length - visible);
                 if (studentIdx < maxIdx) { studentIdx++; updateStudentSlider(); }
            });
            window.addEventListener('resize', updateStudentSlider);
        }

        // =========================================
        // LOGIC: MAIN SLIDER FACTORY (Works for both Clubs & Committees)
        // =========================================
        function initMainSlider(opts) {
            const { row, prevBtn, nextBtn, dotsContainer, totalCards, desktopVis, dataObj, detailEls, onOpen } = opts;
            let curPage = 0;
            let totalPages = 1;

            function calcPages() {
                const cardsPerView = (window.innerWidth >= 992) ? desktopVis : (window.innerWidth >= 768 ? 3 : 2);
                totalPages = Math.ceil(totalCards / cardsPerView);
                // Ensure current page doesn't exceed new total pages after resize
                if (curPage >= totalPages) curPage = totalPages - 1;
            }

            function render() {
                 // Use percentage based translation for smoother responsive behavior
                 // Assuming roughly equal gaps. Adjust '15px' if your CSS gap changes significantly.
                 row.style.transform = `translateX(calc(-${curPage} * (100% + 15px)))`;
                 
                 if (prevBtn) prevBtn.disabled = (curPage <= 0);
                 if (nextBtn) nextBtn.disabled = (curPage >= totalPages - 1);

                 if (dotsContainer) {
                     dotsContainer.innerHTML = '';
                     for (let i = 0; i < totalPages; i++) {
                         const dot = document.createElement('li');
                         dot.className = `dot ${i === curPage ? 'active' : ''}`;
                         dot.onclick = () => { curPage = i; render(); };
                         dotsContainer.appendChild(dot);
                     }
                 }
            }

            if (prevBtn) prevBtn.onclick = () => { if (curPage > 0) curPage--; render(); };
            if (nextBtn) nextBtn.onclick = () => { if (curPage < totalPages - 1) curPage++; render(); };

            window.addEventListener('resize', () => { calcPages(); render(); });

            // --- Open Card Handler ---
            function open(id, cardEl) {
                const item = dataObj[id];
                if (!item) return;

                // Active state
                row.querySelectorAll('.image-card').forEach(c => c.classList.remove('active'));
                if (cardEl) cardEl.classList.add('active');

                // Fill Basic Details
                if (detailEls.title) detailEls.title.innerHTML = item.title;
                if (detailEls.content) detailEls.content.innerHTML = item.content;
                
                // Fill Links (if applicable)
                if (detailEls.links) {
                    detailEls.links.innerHTML = '';
                    if (item.links) {
                        item.links.forEach(l => {
                            detailEls.links.innerHTML += `
                                <a href="${l.url}" target="_blank" class="btn btn-sm btn-outline-warning me-2 mb-2">
                                    ${l.icon ? '<i class="' + l.icon + '"></i>' : (l.text || 'Visit')}
                                </a>`;
                        });
                    }
                }

                // Show Detail Card
                if (detailEls.card) detailEls.card.style.display = 'block';

                // Run custom hook (for committees to load mentors/students)
                if (onOpen) onOpen(item);
            }

            // Attach click events
            row.querySelectorAll('.image-card').forEach(card => {
                card.onclick = () => open(card.dataset.card, card);
            });

            // Init
            calcPages();
            render();
            // Open first card initially
            const first = row.querySelector(".image-card[data-card='1']");
            if (first) open('1', first);
        }

        // =========================================
        // INITIALIZE EVERYTHING
        // =========================================

        // 1. INIT CLUBS SLIDER
        initMainSlider({
            row: document.getElementById('clubsRow'),
            prevBtn: document.getElementById('prevBtnClubs'),
            nextBtn: document.getElementById('nextBtnClubs'),
            dotsContainer: document.getElementById('dotsClubs'),
            totalCards: 8, desktopVis: 8, dataObj: clubData,
            detailEls: {
                card: document.getElementById('detailCardClubs'),
                title: document.getElementById('detailTitleClubs'),
                content: document.getElementById('detailContentClubs'),
                links: document.getElementById('detailLinksClubs')
            }
        });

        // 2. INIT COMMITTEES SLIDER (With EXTRA onOpen logic)
        initMainSlider({
            row: document.getElementById('committeesRow'),
            prevBtn: document.getElementById('prevBtnCommittee'),
            nextBtn: document.getElementById('nextBtnCommittee'),
            dotsContainer: document.getElementById('dotsCommittee'),
            totalCards: 6, desktopVis: 6, dataObj: committeeData,
            detailEls: {
                card: document.getElementById('detailCardCommittee'),
                title: document.getElementById('detailTitleCommittee'),
                content: document.getElementById('detailContentCommittee'),
                links: document.getElementById('detailLinksCommittee')
            },
            onOpen: (item) => {
                // A. Render Mentor
                const mContainer = document.getElementById('mentorContainer');
                if (mContainer) {
                     mContainer.innerHTML = item.mentor ? `
                        <div class="mentor-wrapper">
                            <div class="member-card">
                                <img src="${item.mentor.img}" alt="Mentor">
                                <div class="member-info"><h5>${item.mentor.name}</h5></div>
                            </div>
                        </div>` : '<p class="text-muted">Not Available</p>';
                }

                // B. Render Students & Reset Sub-Slider
                if (studentTrack && item.students) {
                    studentTrack.innerHTML = item.students.map(s => `
                        <div class="student-card-wrapper">
                            <div class="member-card">
                                <span class="batch-badge">${s.batch}</span>
                                <img src="${s.img}" alt="${s.name}">
                                <div class="member-info"><h5>${s.name}</h5></div>
                            </div>
                        </div>
                    `).join('');
                    
                    studentIdx = 0; // Reset index
                    setTimeout(updateStudentSlider, 100); // Slight delay for DOM rendering
                }
            }
        });

    }); // End DOMContentLoaded
