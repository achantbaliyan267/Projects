    <script>
    document.addEventListener("DOMContentLoaded", function () {
    const searchInput = document.getElementById("facultySearch");
    const deptButtons = document.querySelectorAll("#deptTabs .nav-link");
    const cards = document.querySelectorAll(".faculty-card-wrap");
    const noResults = document.getElementById("noResultsMsg");
    let currentDept = "all";

    // Text search
    searchInput.addEventListener("keyup", filterFaculty);

    // Department filter
    deptButtons.forEach(btn => {
        btn.addEventListener("click", function () {
        deptButtons.forEach(b => b.classList.remove("active"));
        this.classList.add("active");
        currentDept = this.dataset.dept;
        filterFaculty();
        });
    });

    function filterFaculty() {
        const query = searchInput.value.toLowerCase();
        let visibleCount = 0;

        cards.forEach(card => {
        const name = card.dataset.name.toLowerCase();
        const dept = card.dataset.dept;
        const matchDept = currentDept === "all" || dept === currentDept;
        const matchName = name.includes(query);
        const show = matchDept && matchName;
        card.style.display = show ? "" : "none";
        if (show) visibleCount++;
        });

        // Show or hide "no results" message
        noResults.style.display = visibleCount === 0 ? "block" : "none";
    }
    });
    </script>


    


