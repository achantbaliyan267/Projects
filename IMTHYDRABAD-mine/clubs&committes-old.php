  // ==================== Generic slider function ====================
  function createSlider(options) {
    const row = options.row;               // DOM element for .cards-row
    const container = options.container;   // slider-container element
    const prevBtn = options.prevBtn;
    const nextBtn = options.nextBtn;
    const dotsContainer = options.dotsContainer;
    const totalCards = options.totalCards;
    const desktopVisible = options.desktopVisible; // number visible on desktop (8 or 6)
    const detail = options.detail; // { card, titleEl, contentEl, linksEl }
    let currentPage = 0;
    let cardsPerView = desktopVisible;
    let totalPages = 1;
    let pageWidth = 0;

    function computeSizes() {
      // determine cardsPerView based on width
      if (window.innerWidth >= 992) cardsPerView = desktopVisible;
      else if (window.innerWidth >= 768) cardsPerView = 3;
      else cardsPerView = 2;
      totalPages = Math.max(1, Math.ceil(totalCards / cardsPerView));

      // compute item width and gap using layout
      const wrapper = row.querySelector('.card-wrapper');
      if (!wrapper) return;
      const wrapperRect = wrapper.getBoundingClientRect();
      const gapStr = window.getComputedStyle(row).gap || window.getComputedStyle(row).columnGap || "15px";
      const gap = parseFloat(gapStr) || 15;
      const itemW = wrapperRect.width;
      pageWidth = itemW * cardsPerView + gap * (cardsPerView - 1);
    }

    function createDots() {
      dotsContainer.innerHTML = '';
      for (let i=0;i<totalPages;i++){
        const li = document.createElement('li');
        li.className = 'dot' + (i===0 ? ' active' : '');
        li.addEventListener('click', ()=> goToPage(i));
        dotsContainer.appendChild(li);
      }
    }

    function updateDots() {
      const dots = dotsContainer.querySelectorAll('.dot');
      dots.forEach((d, idx) => d.classList.toggle('active', idx === currentPage));
    }

    function goToPage(pageIndex, animate=true) {
      currentPage = Math.max(0, Math.min(pageIndex, totalPages-1));
      if (cardsPerView >= totalCards) {
        row.style.transform = `translateX(0px)`;
      } else {
        row.style.transform = `translateX(-${currentPage * pageWidth}px)`;
      }
      updateDots();
      prevBtn.disabled = (currentPage === 0);
      nextBtn.disabled = (currentPage === totalPages - 1);
    }

    // prev/next
    prevBtn.addEventListener('click', ()=> { if (currentPage>0) goToPage(currentPage-1); });
    nextBtn.addEventListener('click', ()=> { if (currentPage<totalPages-1) goToPage(currentPage+1); });

    // swipe handling
    let startX = 0, isPointerDown = false;
    container.addEventListener('touchstart', (e)=> { startX = e.touches[0].clientX; isPointerDown=true; }, {passive:true});
    container.addEventListener('touchend', (e)=> {
      if (!isPointerDown) return;
      const endX = e.changedTouches[0].clientX;
      isPointerDown = false;
      const diff = startX - endX;
      if (diff > 50 && currentPage < totalPages-1) goToPage(currentPage+1);
      else if (diff < -50 && currentPage > 0) goToPage(currentPage-1);
    });

    // mouse drag support (desktop)
    container.addEventListener('mousedown', (e)=> { startX = e.clientX; isPointerDown=true; });
    container.addEventListener('mouseup', (e)=> {
      if (!isPointerDown) return;
      const endX = e.clientX; isPointerDown=false; const diff = startX - endX;
      if (diff > 50 && currentPage < totalPages-1) goToPage(currentPage+1);
      else if (diff < -50 && currentPage > 0) goToPage(currentPage-1);
    });
    container.addEventListener('mouseleave', ()=> { isPointerDown=false; });

    // Function to open a card by ID
    function openCard(cardId) {
      const dataObj = options.dataObj;
      const item = dataObj && dataObj[cardId];
      if (!item) return;
      
      // set title & content with HTML
      detail.titleEl.innerHTML = `<strong>${item.title}</strong>`;
      detail.contentEl.innerHTML = item.content || '';
      detail.linksEl.innerHTML = '';
      if (Array.isArray(item.links)) {
        item.links.forEach(l => {
          const a = document.createElement('a');
          a.className = 'btn btn-sm btn-outline-primary me-2 mt-2';
          a.href = l.url || '#';
          a.target = '_blank';
          a.rel = 'noopener noreferrer';
          a.textContent = l.text;
          detail.linksEl.appendChild(a);
        });
      }
      detail.card.style.display = 'block';
    }

    // clicking cards to open detail
    row.querySelectorAll('.image-card').forEach(card => {
      card.addEventListener('click', function () {
        const id = this.getAttribute('data-card');
        openCard(id);
      });
    });

    // close button


    // initialize (run after a small delay so layout is stable)
    function initialize(){
      // recalc sizes after layout
      setTimeout(()=> {
        computeSizes();
        createDots();
        goToPage(0);
        // hide controls on desktop if all visible
        if (cardsPerView >= totalCards) {
          options.prevBtn.parentElement.style.display = 'none';
        } else {
          options.prevBtn.parentElement.style.display = '';
        }
        
        // Auto-open first card if specified
        if (options.openFirstCard) {
          openCard('1');
        }
      }, 40);
    }

    // recalc on resize
    window.addEventListener('resize', ()=> { initialize(); });

    // expose init
    initialize();
    
    // Return the openCard function for external access
    return {
      openCard: openCard
    };
  }

